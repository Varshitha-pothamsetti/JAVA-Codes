class ArithemeticOperations {
    static void add(int a,int b){
        System.out.println(a+b);
    }
    static void subtract(int a,int b){
        System.out.println(a-b);
    }
    static void multiply(int a,int b){
        System.out.println(a*b);
    }
    static void divide(int a,int b){
        System.out.println(a/b);
    }
}
class Example1{
    public static void main(String[] args){
      //ArithemeticOperations = new ArithemeticOperations ();
      ArithemeticOperations .add(10,20);
      ArithemeticOperations .subtract(10,20);
      ArithemeticOperations .multiply(10,20);
      ArithemeticOperations .divide(10,20);

    }
}
